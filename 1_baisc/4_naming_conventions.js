/**
 * Naming Conventions
 *
 * 1) 일반적로 영어(라틴문자)를 사용하며 문자와 숫자를 모두 사용 할 수 있다.
 * 2) 특수문자는 $, _만 사용 가능
 * 3) 숫자로 시작할 수 없다. - 에러남
 * 4) 키워드는 변수명으로 사용할 수 없다.
 *    var let const function return if else for while switch case break continue...
 */
let codefactory = "코드팩토리";
var $ive = "아이브";
const _yuJin = "안유진";

console.log(codefactory);
console.log($ive);
console.log(_yuJin);

